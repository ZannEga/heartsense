# 1) Install dependencies -----------------------------------------------
# !pip install -q pytorch-tabnet torch onnx onnx2tf onnx_graphsurgeon sng4onnx onnxsim tensorflow

import numpy as np
import pandas as pd
import torch
from pytorch_tabnet.tab_model import TabNetRegressor

# 2) Load the trained TabNet model ---------------------------------------
model = TabNetRegressor()
model.load_model("tabnet_model.zip")  # adjust path if needed

# IMPORTANT: TabNetRegressor.network.forward() returns (output, M_loss).
# We only want the prediction, so wrap it in a module that returns a single
# tensor - this also makes the resulting ONNX/TFLite graph simpler.
class TabNetWrapper(torch.nn.Module):
    def __init__(self, tabnet_network):
        super().__init__()
        self.tabnet_network = tabnet_network

    def forward(self, x):
        out, _ = self.tabnet_network(x)
        return out

wrapper = TabNetWrapper(model.network)
wrapper.eval()

NUM_FEATURES = 14  # sex, age, cp, resting_BP, chol, fbs, restecg, thalach,
                    # exang, oldpeak, slope, ca, thal, Max Heart Rate Reserve
dummy_input = torch.randn(1, NUM_FEATURES, dtype=torch.float32)

# 3) Export to ONNX -------------------------------------------------------
# NOTE: dynamo=False + opset_version=17 is required here - the newer
# "dynamo" ONNX exporter (PyTorch's default since ~2.x) fails on TabNet's
# internal ops (hits a constant-folding crash on a SplitToSequence node
# from the entmax/sparsemax masking). Forcing the older TorchScript-based
# exporter avoids that entirely. This was verified working on the
# previous tabnet_model.zip checkpoint - same architecture here, so it
# should convert the same way.
torch.onnx.export(
    wrapper,
    dummy_input,
    "tabnet_model.onnx",
    input_names=["input"],
    output_names=["risk_score"],
    dynamic_axes={"input": {0: "batch"}, "risk_score": {0: "batch"}},
    opset_version=17,
    dynamo=False,
)
print("ONNX export done: tabnet_model.onnx")

# 4) Convert ONNX -> TensorFlow SavedModel -> TFLite ---------------------
# onnx2tf is the actively-maintained tool for this (better op coverage
# than the older onnx-tf). Run from a shell cell in Colab:
#   !onnx2tf -i tabnet_model.onnx -o tabnet_saved_model
#
# This produces several .tflite variants inside tabnet_saved_model/.
# Use the plain float32 one, usually named:
#   tabnet_saved_model/tabnet_model_float32.tflite
