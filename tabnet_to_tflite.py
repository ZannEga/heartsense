# 5) Sanity check: compare PyTorch vs TFLite outputs on real data --------
# Run this AFTER you have the .tflite file, to make sure the conversion
# didn't change the model's behavior.
def sanity_check(tflite_path, test_scaled_csv):
    import tensorflow as tf

    df = pd.read_csv(test_scaled_csv)
    X = df.drop(columns=["Heart Disease Risk Score"]).values.astype(np.float32)
    y = df["Heart Disease Risk Score"].values

    # PyTorch predictions
    torch_preds = model.predict(X).flatten()

    # TFLite predictions
    interpreter = tf.lite.Interpreter(model_path=tflite_path)
    interpreter.allocate_tensors()
    inp = interpreter.get_input_details()[0]
    out = interpreter.get_output_details()[0]

    tflite_preds = []
    for row in X:
        interpreter.set_tensor(inp["index"], row.reshape(1, -1))
        interpreter.invoke()
        tflite_preds.append(interpreter.get_tensor(out["index"]).flatten()[0])
    tflite_preds = np.array(tflite_preds)

    diff = np.abs(torch_preds - tflite_preds)
    print("Max abs diff  PyTorch vs TFLite:", diff.max())
    print("Mean abs diff PyTorch vs TFLite:", diff.mean())
    print("Sample compare (first 5):")
    for i in range(5):
        print(f"  actual={y[i]:.2f}  torch={torch_preds[i]:.2f}  tflite={tflite_preds[i]:.2f}")

